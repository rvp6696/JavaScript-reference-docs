

// Time events in JS

/*

The window object allows execution of code at specified time intervals.

These time intervals are called timing events.

The two main key methods are : 

    1. setTInterval(function,millisecond)   => executes a function, after waiting for a specific no of milliseconds.

    2. setTimeout(function,millisecond)    => same as above, but repeats the execution of function continuously.

Below two methods are less imp:

    3. clearTimeout(data)    => stops the setTimeout, but make sure you store the setTimeout in a variable

    4. clearInterval(data)       => works same as above but stops setInterval
*/

